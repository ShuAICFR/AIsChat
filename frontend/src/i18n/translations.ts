export const translations: Record<'zh' | 'en', TranslationDict> = { zh, en }

/**
 * 深度获取翻译值：t('sidebar.chat')
 */
export function getTranslation(
  lang: 'zh' | 'en',
  path: string
): string {
  // 扁平字典直接查找（key 本身就是 'nav.chat' 这种带点的字符串）
  const dict = translations[lang] || translations.zh
  const val = (dict as Record<string, unknown>)[path]
  if (typeof val === 'string') return val
  // fallback to zh
  const fb = (translations.zh as Record<string, unknown>)[path]
  if (typeof fb === 'string') return fb
  return path
}
